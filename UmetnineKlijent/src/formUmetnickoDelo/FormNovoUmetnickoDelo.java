    private void popuniTipoveDela() {

        try {
            ArrayList<TipDela> tipoviDela = ClientController.getInstance().getAllTipDela();

            cmbTipDela.removeAllItems();

            for (TipDela tipDela : tipoviDela) {
                cmbTipDela.addItem(tipDela);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNovoUmetnickoDelo.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
