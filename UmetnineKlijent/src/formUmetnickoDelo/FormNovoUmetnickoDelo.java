    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtNaziv.getText().isEmpty() || txtCena.getText().isEmpty()
                    || txtOpis.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }
            if (!rbDa.isSelected() && !rbNe.isSelected()) {
                JOptionPane.showMessageDialog(null, "Izaberite da li je delo unikatno ili ne!");
                return;
            }

            TipDela td = (TipDela) cmbTipDela.getSelectedItem();
            String naziv = txtNaziv.getText();
            double cena = Double.parseDouble(txtCena.getText());
            String opis = txtOpis.getText();
            boolean unikat = rbDa.isSelected();

            TableModelMaterijali tm = (TableModelMaterijali) tblMaterijali.getModel();

            UmetnickoDelo ud = new UmetnickoDelo(null, naziv, cena, opis, unikat, td, tm.getLista());

            ClientController.getInstance().addUmetnickoDelo(ud);
            MainForm mf = (MainForm) getParent();
            mf.popuniUmetnickaDela(ud.getTipDela());
            mf.popuniTipoveDela();
            JOptionPane.showMessageDialog(this, "Uspesno dodato umetnicko delo.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnDodajMaterijalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajMaterijalActionPerformed

        if (txtNazivMaterijala.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti naziv materijala!");
            return;
        }

        TableModelMaterijali tm = (TableModelMaterijali) tblMaterijali.getModel();

        String nazivMaterijala = txtNazivMaterijala.getText();

        if (tm.postojiMaterijal(nazivMaterijala)) {
            JOptionPane.showMessageDialog(this, "Vec ste uneli ovaj materijal!");
            return;
        }

        Materijal m = new Materijal(null, -1, nazivMaterijala);

        tm.dodajMaterijal(m);

        txtNazivMaterijala.setText("");


    }//GEN-LAST:event_btnDodajMaterijalActionPerformed

    private void btnObrisiMaterijalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiMaterijalActionPerformed

        int row = tblMaterijali.getSelectedRow();

        if (row >= 0) {
            TableModelMaterijali tm = (TableModelMaterijali) tblMaterijali.getModel();
            tm.obrisiMaterijal(row);
        }

    }//GEN-LAST:event_btnObrisiMaterijalActionPerformed

